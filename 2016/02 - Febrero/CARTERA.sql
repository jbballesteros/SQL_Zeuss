FROM v_cartera_financiera
WHERE NIT NOT IN (42784878,
3512448,
71759374,
900472121,
900536589,
811003895,
37259645,
800053516,
75040084,
71586305,
70250687)
GROUP BY nit,CLIENTE
ORDER BY  nit,CLIENTE

SELECT *,ISNULL(P.SALDO_INICIAL,T.SALDO)*-1,ISNULL(P.SALDO_FINAL,0),ISNULL(T.SALDO,0)
FROM (
SELECT T.nit,T.nombres,SUM(CV.saldo_inicial) SALDO_INICIAL,SUM(CV.debito) DEBITO,SUM(CV.credito) CREDITO,SUM(CV.saldo_inicial+CV.debito-CV.credito) SALDO_FINAL
FROM cuentas_val CV 
INNER JOIN terceros T ON (CV.nit=T.nit)
WHERE CV.ano=2015 AND CV.mes=5 AND CV.cuenta='130505'
GROUP BY T.nit,T.nombres ) AS P FULL JOIN ( 

SELECT nit,CLIENTE,SUM(SALDO) SALDO
FROM v_cartera_financiera
WHERE NIT NOT IN (42784878,
3512448,
71759374,
900472121,
900536589,
811003895,
37259645,
800053516,
75040084,
71586305,
70250687)
GROUP BY nit,CLIENTE) AS T ON (P.NIT=T.nit)