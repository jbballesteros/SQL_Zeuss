
SELECT AÑO, P.MES, T.SALDO, 
ROUND((ROUND(POR_VENCER/P.SALDO,2))*T.SALDO , 2) POR_VENCER, 
ROUND((ROUND(DE_1_A_5/P.SALDO,2))*T.SALDO  , 2) DE_1_A_5, 
ROUND((ROUND(DE_6_A_10/P.SALDO,2))*T.SALDO , 2) DE_6_A_10, 
ROUND((ROUND(DE_11_A_15/P.SALDO,2))*T.SALDO , 2) DE_11_A_15, 
ROUND((ROUND(de_16_a_30/P.SALDO,2))*T.SALDO , 2) de_16_a_30, 
ROUND((ROUND(DE_31_A_60/P.SALDO,2))*T.SALDO  , 2) DE_31_A_60, 
ROUND((ROUND(DE_61_A_90/P.SALDO,2))*T.SALDO , 2) DE_61_A_90, 
ROUND((ROUND(DE_91_A_120/P.SALDO,2))*T.SALDO , 2) DE_91_A_120, 
ROUND((ROUND(MAS_DE_120/P.SALDO,2))*T.SALDO , 2) MAS_DE_120
FROM (

SELECT ano AÑO,mes MES,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN Saldo ELSE Saldo*-1 END),0) AS DECIMAL(20,5)) SALDO,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN Sin_Vencer ELSE Sin_Vencer*-1 END),0) AS DECIMAL(20,5)) POR_VENCER,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN de_1_a_5 ELSE de_1_a_5*-1 END),0) AS DECIMAL(20,5)) DE_1_A_5,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN de_6_a_10 ELSE de_6_a_10*-1 END),0)  AS DECIMAL(20,5)) DE_6_A_10,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN de_11_a_15 ELSE de_11_a_15*-1 END),0)  AS DECIMAL(20,5)) DE_11_A_15,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN de_16_a_30 ELSE de_16_a_30*-1 END),0)AS DECIMAL(20,5)) de_16_a_30,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN de_31_a_60 ELSE de_31_a_60*-1 END),0)AS DECIMAL(20,5)) DE_31_A_60,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN de_61_a_90 ELSE de_61_a_90*-1 END),0)AS DECIMAL(20,5)) DE_61_A_90,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN de_91_a_120 ELSE de_91_a_120*-1 END),0)AS DECIMAL(20,5)) DE_91_A_120,
CAST(ROUND(SUM(CASE WHEN SW IN (1,6,4,21,22,31) THEN Mas_de_120 ELSE Mas_de_120*-1 END),0)AS DECIMAL(20,5)) MAS_DE_120
FROM v_cartera_edades_corte
WHERE  sw IN (1,2,5,22,23) AND ano=2016 AND mes=6
GROUP BY ano,mes) AS P INNER JOIN (SELECT ano,mes,CAST(SUM(saldo_inicial+debito-credito) AS DECIMAL(20,5)) SALDO
FROM cuentas_val 
 WHERE cuenta='130505'
GROUP BY ano,mes) AS T on (P.AÑO=T.ano AND P.MES=T.mes)
 