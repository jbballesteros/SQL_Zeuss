SELECT P.CONCEPTO,TM.TIPO,TM.NIT,TM.DIGITO,TM.DIGITO,TM.PAPELLIDO,TM.SAPELLIDO,TM.PNOMBRE,TM.SNOMBRE,
TM.RAZON_SOCIAL,TM.PAIS,SUM(P.SALDOFINAL) SALDOFINAL,TM2.TIPO,TM2.NIT,TM2.PAPELLIDO,TM2.SAPELLIDO,TM2.PNOMBRE,TM2.SNOMBRE,TM2.RAZON_SOCIAL,TM2.DIRECCION,TM2.DEPARTAMENTO,TM2.CIUDAD,TM2.PAIS
FROM (

SELECT 
 4070  CONCEPTO,
T.nit_real NIT, 
ROUND(CV.credito,0) SALDOFINAL,
CASE WHEN CUENTA='28150501' THEN 800067604 
WHEN CUENTA='28150504' THEN 811043174
WHEN CUENTA='28150505' THEN 890911585
WHEN CUENTA='28150506' THEN 899999022
ELSE 0 END NITBEN
FROM cuentas_val CV 
INNER JOIN terceros T ON (CV.nit=T.nit) 
WHERE CV.ano=2016 AND CV.mes<>13 AND 
CV.cuenta IN ('28150501','28150504','28150505','28150506')
AND  (CV.credito)<>0

) AS P 
LEFT JOIN terceros_medios TM ON (P.NIT=TM.NIT)
LEFT JOIN terceros_medios TM2 ON (P.NITBEN=TM2.NIT)

GROUP BY P.CONCEPTO,TM.TIPO,TM.NIT,TM.DIGITO,TM.DIGITO,TM.PAPELLIDO,TM.SAPELLIDO,TM.PNOMBRE,TM.SNOMBRE,
TM.RAZON_SOCIAL,TM.PAIS,TM2.TIPO,TM2.NIT,TM2.PAPELLIDO,TM2.SAPELLIDO,TM2.PNOMBRE,TM2.SNOMBRE,TM2.RAZON_SOCIAL,TM2.DIRECCION,TM2.DEPARTAMENTO,TM2.CIUDAD,TM2.PAIS