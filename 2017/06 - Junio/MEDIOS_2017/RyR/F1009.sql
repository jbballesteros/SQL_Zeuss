SELECT P.CONCEPTO CONCEPTO,TM.TIPO,ISNULL(TM.NIT,P.NIT_REAL) NIT,TM.DIGITO,TM.PAPELLIDO,TM.SAPELLIDO,TM.PNOMBRE,TM.SNOMBRE,TM.RAZON_SOCIAL,TM.DIRECCION,TM.DEPARTAMENTO,TM.CIUDAD,TM.PAIS,SUM(P.VALOR) VALOR
FROM (

SELECT 
CASE 
WHEN CV.cuenta BETWEEN '2205' AND '2210ZZZZ' THEN 2201 
WHEN CV.cuenta BETWEEN '2355' AND '2355ZZZZ' THEN 2202
WHEN CV.cuenta BETWEEN '2105' AND '212015ZZZZ' THEN 2203
WHEN CV.cuenta BETWEEN '25' AND '25ZZZZZZ' THEN 2205
WHEN CV.cuenta BETWEEN '234502' AND '234502ZZZZZ' THEN 2206
WHEN CV.cuenta BETWEEN '2335' AND '233595ZZZZ' THEN 2206
WHEN CV.cuenta BETWEEN '2195' AND '2195ZZZZ' THEN 2206
WHEN CV.cuenta BETWEEN '289505' AND '289505' THEN 2206 
ELSE 0 END CONCEPTO,T.nit_real,ROUND(SUM(CV.saldo_inicial+CV.debito-CV.credito)*-1,0) VALOR
FROM cuentas_val CV INNER JOIN terceros T ON (CV.nit=T.nit)
WHERE CV.ano=2016 AND CV.mes=12 AND 
(CV.cuenta BETWEEN '2205' AND '2210ZZZZ' OR
CV.cuenta BETWEEN '2355' AND '2355ZZZZ' OR
CV.cuenta BETWEEN '2105' AND '212015ZZZZ' OR
CV.cuenta BETWEEN '25' AND '25ZZZZZZ' OR
CV.cuenta BETWEEN '234502' AND '234502ZZZZZ' OR
CV.cuenta BETWEEN '2335' AND '233595ZZZZ' OR
CV.cuenta BETWEEN '2195' AND '2195ZZZZ' OR
CV.cuenta BETWEEN '289505' AND '289505')
GROUP BY CV.cuenta,T.nit_real
HAVING  SUM(CV.saldo_inicial+CV.debito-CV.credito)<>0
) AS P LEFT JOIN terceros_medios TM ON (P.nit_real=TM.NIT)
GROUP BY P.CONCEPTO ,TM.TIPO,P.nit_real,TM.NIT,TM.DIGITO,TM.PAPELLIDO,TM.SAPELLIDO,TM.PNOMBRE,TM.SNOMBRE,TM.RAZON_SOCIAL,TM.DIRECCION,TM.DEPARTAMENTO,TM.CIUDAD,TM.PAIS
ORDER BY P.CONCEPTO,TM.NIT